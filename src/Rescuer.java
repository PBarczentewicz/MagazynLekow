public class Rescuer {

    int idRescuer;
    String name;
    String surname;
    String degree;
    Boolean medicalProfession;
    Boolean buyer;


    public Rescuer(int idRescuer, String name, String surname, String degree, Boolean medicalProfession, Boolean buyer) {
        this.idRescuer = idRescuer;
        this.name = name;
        this.surname = surname;
        this.degree = degree;
        this.medicalProfession = medicalProfession;
        this.buyer = buyer;

    }
}



